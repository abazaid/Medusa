export default async function publishSaudiBeginnerVapeBlog({
  container,
}: ExecArgs) {
  const logger = container.resolve("logger") as {
    info: (msg: string) => void
    error: (msg: string) => void
  }
  const blogService = container.resolve(BLOG_MODULE) as any

  const canonicalUrl = `https://vapehubksa.com/ar/blog/${HANDLE}`
  const nowIso = new Date().toISOString()

  const existing = await blogService.listPosts({ handle: HANDLE }, { take: 1 } as any)

  if (existing?.length) {
    const current = existing[0]
    await blogService.updatePosts({
      id: current.id,
      handle: HANDLE,
      status: "published",
      title_ar: titleAr,
      title_en: "Best Beginner Vapes in Saudi Arabia 2026",
      excerpt_ar: excerptAr,
      excerpt_en:
        "A practical Saudi-focused guide to choosing your first vape device, nicotine strength, and related products.",
      content_ar: contentAr,
      content_en: "",
      cover_image: "/blog/best-beginner-vapes-saudi-2026.svg",
      meta_title_ar: metaTitleAr,
      meta_title_en: "Best Beginner Vape Guide Saudi Arabia 2026",
      meta_description_ar: metaDescriptionAr,
      meta_description_en:
        "Saudi beginner vape guide for 2026 with practical comparisons, mistakes to avoid, and internal store recommendations.",
      canonical_url: canonicalUrl,
      published_at: current.published_at || nowIso,
      updated_by: "codex",
    })
    logger.info(`Blog post updated: ${HANDLE}`)
    return
  }

  await blogService.createPosts({
    handle: HANDLE,
    status: "published",
    title_ar: titleAr,
    title_en: "Best Beginner Vapes in Saudi Arabia 2026",
    excerpt_ar: excerptAr,
    excerpt_en:
      "A practical Saudi-focused guide to choosing your first vape device, nicotine strength, and related products.",
    content_ar: contentAr,
    content_en: "",
    cover_image: "/blog/best-beginner-vapes-saudi-2026.svg",
    meta_title_ar: metaTitleAr,
    meta_title_en: "Best Beginner Vape Guide Saudi Arabia 2026",
    meta_description_ar: metaDescriptionAr,
    meta_description_en:
      "Saudi beginner vape guide for 2026 with practical comparisons, mistakes to avoid, and internal store recommendations.",
    canonical_url: canonicalUrl,
    published_at: nowIso,
    created_by: "codex",
    updated_by: "codex",
  })

  logger.info(`Blog post created: ${HANDLE}`)
}
